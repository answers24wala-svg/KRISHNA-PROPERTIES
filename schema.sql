-- 1. Create the properties table
CREATE TABLE IF NOT EXISTS public.properties (
    id TEXT PRIMARY KEY,
    title TEXT NOT NULL,
    location TEXT NOT NULL,
    price NUMERIC NOT NULL,
    currency TEXT NOT NULL DEFAULT 'INR',
    price_formatted TEXT NOT NULL,
    area_sqft NUMERIC NOT NULL,
    property_type TEXT NOT NULL,
    listing_status TEXT NOT NULL,
    bedrooms NUMERIC NOT NULL DEFAULT 0,
    bathrooms NUMERIC NOT NULL DEFAULT 0,
    parking NUMERIC NOT NULL DEFAULT 0,
    is_featured BOOLEAN NOT NULL DEFAULT false,
    is_verified BOOLEAN NOT NULL DEFAULT false,
    is_new_listing BOOLEAN NOT NULL DEFAULT true,
    is_furnished BOOLEAN NOT NULL DEFAULT false,
    is_gated_community BOOLEAN NOT NULL DEFAULT false,
    amenities JSONB NOT NULL DEFAULT '[]'::jsonb,
    description TEXT NOT NULL,
    images JSONB NOT NULL DEFAULT '[]'::jsonb,
    agent JSONB NOT NULL,
    nearby JSONB NOT NULL DEFAULT '[]'::jsonb,
    created_at TIMESTAMP WITH TIME ZONE DEFAULT timezone('utc'::text, now()) NOT NULL
);

-- Enable RLS
ALTER TABLE public.properties ENABLE ROW LEVEL SECURITY;

